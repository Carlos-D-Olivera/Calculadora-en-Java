    private void signoMas(){
        String t = this.textoPantalla.getText();
         
        if(t.isEmpty()){
            return;
        }
        if(t.endsWith("-")|| t.endsWith("/")|| t.endsWith("*")){
            t = t.substring(0, t.length()-1);
        }else if(t.endsWith("+")){
            return;
        }
        t += "+";
        this.ecuacion = t;
        mostrar();
    }

