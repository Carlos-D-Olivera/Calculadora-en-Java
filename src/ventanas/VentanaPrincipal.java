    private void signoPunto(){
        if(this.ecuacion.isEmpty()){
            this.ecuacion = "0.";
            mostrar();
        }
        if(this.ecuacion.charAt(this.ecuacion.length()-1) != '.'){
            
            this.introducir("."); 
        }     
    }
    
    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        
        switch(evt.getKeyCode()){
            case KeyEvent.VK_0, KeyEvent.VK_NUMPAD0 -> this.introducir("0");
            case KeyEvent.VK_1, KeyEvent.VK_NUMPAD1 -> this.introducir("1");
            case KeyEvent.VK_2, KeyEvent.VK_NUMPAD2 -> this.introducir("2");
            case KeyEvent.VK_3, KeyEvent.VK_NUMPAD3 -> this.introducir("3");
            case KeyEvent.VK_4, KeyEvent.VK_NUMPAD4 -> this.introducir("4");
            case KeyEvent.VK_5, KeyEvent.VK_NUMPAD5 -> this.introducir("5");
            case KeyEvent.VK_6, KeyEvent.VK_NUMPAD6 -> this.introducir("6");
            case KeyEvent.VK_7, KeyEvent.VK_NUMPAD7 -> this.introducir("7");
            case KeyEvent.VK_8, KeyEvent.VK_NUMPAD8 -> this.introducir("8");
            case KeyEvent.VK_9, KeyEvent.VK_NUMPAD9 -> this.introducir("9");
            case KeyEvent.VK_PLUS, KeyEvent.VK_ADD -> signoMas();
            case KeyEvent.VK_SUBTRACT, KeyEvent.VK_MINUS -> signoMenos();
            case KeyEvent.VK_MULTIPLY, KeyEvent.VK_ASTERISK-> signoPor(); 
            case KeyEvent.VK_DIVIDE, KeyEvent.VK_SLASH -> signoEntre();
            case KeyEvent.VK_ENTER -> calcular();
            case KeyEvent.VK_PERIOD, KeyEvent.VK_DECIMAL -> signoPunto();
            case KeyEvent.VK_BACK_SPACE -> borrar();
            
        }
        
    }//GEN-LAST:event_formKeyPressed


    


