    private void calcular(){
        if(this.ecuacion.isEmpty()){
            return;
        }
        try{
            Ecuacion ec = new Ecuacion(this.ecuacion);
            String res = String.valueOf(ec.getResultado());
            this.listaPantalla.setText(this.ecuacion += " =");
            if(res.endsWith(".0")){
                res = res.substring(0, res.length()-2);
            }

            this.ecuacion = String.valueOf(res);
            mostrar();
            this.ecuacion = "";
        }catch(java.lang.NumberFormatException e){
            this.textoPantalla.setText("ESCRIBA BIEN OMBE");
            System.out.println(e);
        }catch(ArithmeticException e){
            this.textoPantalla.setText("ERROR MATEMATICO");
            System.out.println(e);
       }
        
    }
    
